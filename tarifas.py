def calcular_isr(base):
    limites = [
        (0.01, 7735.00, 0.0192),
        (7735.01, 65651.07, 0.064),
        (65651.08, 115375.90, 0.1088),
        (115375.91, 134119.41, 0.16),
        (134119.42, 160577.65, 0.1792),
        (160577.66, 323862.00, 0.2136),
        (323862.01, 510451.00, 0.2352),
        (510451.01, 974535.03, 0.30),
        (974535.04, 1299380.04, 0.32),
        (1299380.05, 3898140.12, 0.34),
        (3898140.13, float("inf"), 0.35),
    ]

    for li, ls, tasa in limites:
        if li <= base <= ls:
            return (base - li) * tasa

    return 0 
